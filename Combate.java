public class Combate {
  private String player1;
  private String player2;

  public Combate(String player1, String player2) {
    this.player1 = player1;
    this.player2 = player2;
  }

  public String get_player1() {
    return player1;
  }

  public String get_player2() {
    return player2;
  }

}
